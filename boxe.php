            <h2 id="boxe1">Qu'est-ce que la boxe anglaise ?</h2>

                <img src="Image/Boxe1.jpeg" alt="Image qu'est-ce que la boxe anglaise ?" width="700" height="441">

                <p>La boxe anglaise, aussi appelée le noble art, est un sport de combat dans lequel deux adversaires, de même catégorie de poids et de même sexe, se rencontrent sur un ring, munis de gants rembourrés afin de limiter le risque de coupure, et s'échangent des coups de poing, portés au visage et au buste.</p>
                <h3>Les sections en boxe</h3>

                    <p>En France, la boxe anglaise se décline en 5 sections, JMB Club proposant la moins risquée, c'est-à-dire la <strong>boxe loisir</strong>: les contacts sont minimes et les compétitions interdites. Elle porte ainsi l'accent sur l'apprentissage des fondamentaux de la boxe et la forme physique. </p>

                <h3>Les techniques</h3>
                    <h4>Les techniques offensives</h4>

                        <p>Cette boxe utilise plusieurs techniques de poing qui peuvent toucher : à la face ; sur le côté de la tête (mais pas à l'arrière) et au corps (de face ou de côté):
                            <ul>
                                <li>Direct : coup de poing direct. Il est de forme pistonnée, fouettée ou balancée-jetée. Il existe des nuances. Ainsi, le jab est un direct à petite course donné avec le poing avant qui sert à tenir à distance l'adversaire alors que le lead est un direct à grande course et très lourd donné avec le poing arrière.</li>
                                <li>Direct court : coup de poing dans l’axe exécuté sur le bas du buste adverse le pouce en haut et à mi-distance. Contrairement à l’uppercut utilisant la poussée des jambes et l’action de l’épaule, il s’exécute avec une activité plus prononcée du coude.</li>
                                <li>Crochet : coup de poing circulaire (crocheté). Il existe d'autres formes de coups circulaires : le swing (appelé également stick-punch) et le half-swing.</li>
                                <li>Uppercut : coup de poing remontant. Il est classé dans la catégorie des coups circulaires, ce qui n’est pas toujours le cas.</li>
                            </ul>
                        Bien sûr il en existe d'autres, mais celles présentées ci-dessous sont les principales.
                        </p>

                    <h4>Les techniques défensives</h4>

                        <p>La défense est un ensemble de comportements destinés à faire échec à l’offensive adverse. Cela comprend les blocages, les déviations, les esquives et les déplacements.</p>
                        <p>On distingue plusieurs objectifs de défense: 
                            <ul>
                                <li>La simple mise en sécurité de ses propres cibles (défense dite passive : couverture neutre, blocage neutre...) ;</li>
                                <li>La réalisation d’actions destinées à utiliser l’activité adverse à son avantage (défense active). Le blocage déviant et la parade chassée réalisés dans le but de déséquilibrer ainsi que le blocage absorbant et l’esquive permettent au boxeur d'engager des ripostes ;</li>
                                <li>La mise en difficulté de réalisations offensives adverses (par le raccourcissement ou l’augmentation de la distance, le verrouillage des armes adverses, la déstabilisation à base de techniques de menace, de leurre...).</li>
                            </ul>
                        </p>
            <h2 id="boxe2">Pourquoi faire de la boxe anglaise ?</h2>

                        

                <img src="Image/Boxe2.jpg" alt="Image pourquoi faire de la boxe anglaise ?" width="700" height="441">

                <p>Même si ce n'est pas le scoop du siècle, rappelons avant tout que la boxe permet de se muscler, de s'affiner et d'apprendre à se défendre. Avec une pratique régulière on peut perdre rapidement de la masse graisseuse, tout en gagnant du muscle et de la force physique. Notre endurance et notre coordination sont décuplées, ce qui ne gâche rien. La boxe est aussi LE sport idéal pour se muscler les cuisses et travailler son cardio.</p>

                <p>Enfin, la boxe anglaise fait autant de bien au corps qu'à l'esprit. Elle permet d'évacuer tout le stress et les tensions accumulées dans notre vie de tous les jours, de canaliser nos énergies négatives pour frapper et extérioriser nos émotions.</p>

                <p>Rapidité, réflexes, précision, mental de gagnante sont nos meilleures armes pour progresser... La boxe s'adresse aux nerveuses, aux stressées, aux colériques, mais aussi aux timides et à celles qui manquent de confiance en soi. Car les poings serrés dans des gants en mousse, on laisse aux vestiaires nos complexes et nos frustrations pour combattre, dans tous les sens du terme, ce qui nous sépare de nos objectifs. </p>

